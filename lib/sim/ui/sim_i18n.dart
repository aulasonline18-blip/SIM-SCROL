Set<String> _canonicalLocalizationKeys() => {
  ..._simStrings.keys,
  ..._enStrings.keys,
  ..._ptStrings.keys,
};

Map<String, String> _completeStringsFor(String code) {
  final normalized = normalizeSimLanguageCode(code);
  final selected = switch (normalized) {
    'pt' => _ptStrings,
    _ => _enStrings,
  };
  return {
    for (final key in _canonicalLocalizationKeys())
      key:
          selected[key] ??
          (normalized == 'pt' ? _simStrings[key] : _enStrings[key]) ??
          _ptStrings[key] ??
          _simStrings[key] ??
          key,
  };
}

Map<String, String> _activeStrings() =>
    _completeStringsFor(_activeLanguageCode);

Map<String, int> debugSimLocalizationKeyCounts() => {
  'pt': _completeStringsFor('pt').length,
  'en': _completeStringsFor('en').length,
};

Map<String, List<String>> debugSimMissingLocalizationKeys() => {
  for (final code in const ['pt', 'en'])
    code: _canonicalLocalizationKeys()
        .where((key) => !_completeStringsFor(code).containsKey(key))
        .toList(),
};

String debugSimLocalizedValue(String code, String key) =>
    _completeStringsFor(normalizeSimLanguageCode(code))[key] ?? key;

String t(String key, [Map<String, dynamic>? params]) {
  var value = _activeLanguageCode == 'pt'
      ? _ptStrings[key] ?? _simStrings[key] ?? _enStrings[key] ?? key
      : _activeStrings()[key] ?? _enStrings[key] ?? _simStrings[key] ?? key;
  if (params != null) {
    params.forEach((k, v) {
      value = value.replaceAll('{$k}', '$v');
    });
  }
  return value;
}
