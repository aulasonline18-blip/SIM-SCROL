// SIM i18n — single source of truth for visible app strings.
// If a key is missing, fallback order is selected language -> English -> legacy
// base -> key. Lesson/generated content keeps using stableLang in the AI payloads.
String _activeLanguageCode = 'pt';

String get simActiveLanguageCode => _activeLanguageCode;

Locale get simActiveLocale => switch (_activeLanguageCode) {
  'pt' => const Locale('pt', 'BR'),
  'es' => const Locale('es'),
  'fr' => const Locale('fr'),
  'de' => const Locale('de'),
  'it' => const Locale('it'),
  'ja' => const Locale('ja'),
  _ => const Locale('en'),
};

String normalizeSimLanguageCode(String? codeOrName) {
  final raw = (codeOrName ?? '').trim().toLowerCase();
  if (raw.isEmpty) return 'en';
  if (raw == 'pt' ||
      raw == 'pt-br' ||
      raw.contains('portugu') ||
      raw.contains('brasil')) {
    return 'pt';
  }
  if (raw == 'es' || raw.contains('spanish') || raw.contains('español')) {
    return 'es';
  }
  if (raw == 'fr' || raw.contains('french') || raw.contains('français')) {
    return 'fr';
  }
  if (raw == 'de' || raw.contains('german') || raw.contains('deutsch')) {
    return 'de';
  }
  if (raw == 'it' || raw.contains('italian') || raw.contains('italiano')) {
    return 'it';
  }
  if (raw == 'ja' || raw.contains('japanese') || raw.contains('日本')) {
    return 'ja';
  }
  if (raw == 'en' || raw.contains('english')) return 'en';
  return 'en';
}

void setSimActiveLanguage(String? codeOrName) {
  _activeLanguageCode = normalizeSimLanguageCode(codeOrName);
}

String stableLangLabelFor(String code, String fallbackName) {
  return switch (normalizeSimLanguageCode(code)) {
    'pt' => 'Portuguese',
    'es' => 'Spanish',
    'fr' => 'French',
    'de' => 'German',
    'it' => 'Italian',
    'ja' => 'Japanese',
    'en' => 'English',
    _ => fallbackName.trim().isEmpty ? 'English' : fallbackName.trim(),
  };
}

